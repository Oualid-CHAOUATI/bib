class MessageTypes
{
  const SUCCES = "success";
  const ERROR = "error";
  const WARINING = "error";
  const INFORMATION = "error";

  const ACCEPTED = [self::SUCCES, self::ERROR, self::WARINING, self::INFORMATION];
}
/**
 * Undocumented function
 *
 * @param[MessageTypes::(SUCCESS | ERROR | WARINING | INFORMATION)] $type
 * @param [type] $message
 * @return void
 */
function createMessage($type, $message)
{
  if (!in_array($type, MessageTypes::ACCEPTED)) return;


  if (!isset($_SESSION["messages"])) $_SESSION["messages"] = [];

  if (!isset($_SESSION["messages"][$type])) $_SESSION["messages"][$type] = [];


  array_push($_SESSION["messages"][$type], $message);
}

function createMessageSuccess($message)
{
  createMessage(MessageTypes::SUCCES, $message);
}

/**
 * empties the Session Messages
 *
 * @return void
 */
function emptyMessages()
{
  $_SESSION["messages"] = [];
}
